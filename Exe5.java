public class Exe5 {
public static void main(String[] args) {
    //Definindo o raio
    double raio = 5.0;

    // Calculando a área do circulo
    double area = Math.PI * Math.pow(raio, 2);

    // Imprimindo a área 
    System.out.println("A área do circulo com raio" + raio + "é: " + area);
  }
}
