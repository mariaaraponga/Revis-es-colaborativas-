public class Exe6 {
    public static void main(String[] args) {
        // Definindo as dimensões do trapézio
        double b1 = 8.0; // Base maior
        double b2 = 5.0; // Base menor
        double h = 6.0;  // Altura

        // Calculando a área do trapézio
        double area = ((b1 + b2) / 2) * h;

        // Imprimindo a área
        System.out.println("A área do trapézio é: " + area);
    }
}

